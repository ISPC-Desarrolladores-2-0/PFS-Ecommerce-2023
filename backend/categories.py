class Category:
    def __init__(self, id_categories, name):
        self.id_categories = id_categories
        self.name = name

    def get_id_categories(self):
        return self.id_categories

    def set_id_categories(self, id_categories):
        self.id_categories = id_categories

    def get_name(self):
        return self.name

    def set_name(self, name):
        self.name = name
