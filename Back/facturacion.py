class facturacion:

    def __init__(self, nombre, apellido, dni, direccion, numCalle, localidad, mp):
        self.nombre = nombre
        self.apellido = apellido
        self.dni = dni
        self.direccion = direccion
        self.numCalle = numCalle
        self.localidad = localidad
        self.mp = mp

        def getNombre(self):
            return self.nombre
        def getApellido(self):
            return self.apellido
        def getDni(self):
            return self.dni
        def getDireccion(self):
            return self.direccion
        def getNumCalle(self):
            return self.numCalle
        def getLocalidad(self):
            return self.localidad
        def getMp(self):
            return self.mp
        
        