class Categoria():

     def __init__(self, IdCategoria, NombreCategoria):
         self.idCategoria = IdCategoria
         self.nombreCategoria = NombreCategoria

         def getIdCategoria(self):
             return self.idCategoria
         def getNombreCategoria(self):
             return self.nombreCategoria
         def setAgregarComicCategoria(self):
             return self.agregarComicCategoria
         def getEliminarComicCategoria(self):
             return self.eliminarComicCategoria
         def getFiltrarListaCategoria(self):
             return self.filtrarListaCategoria
         def setFiltrarListaCategoria(self):
             return self.filtrarListaCategoria